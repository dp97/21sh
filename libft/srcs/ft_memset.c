#include "libft.h"

void				*ft_memset(void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	*ch;

	ch = str;
	i = 0;
	while (i < n)
	{
		*ch = c;
		ch++;
		i++;
	}
	return (str);
}
