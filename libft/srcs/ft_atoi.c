#include "libft.h"

int		ft_atoi(const char *s)
{
	int		num;
	int		i;
	int		sign;

	i = 0;
	num = 0;
	sign = 1;
	while (s[i] <= 32)
		i++;
	if (s[i] == 45)
	{
		sign = -1;
		i++;
	}
	else if (s[i] == 43)
		i++;
	while (s[i] && s[i] >= 48 && s[i] <= 57)
	{
		num += s[i] - '0';
		if (s[i + 1] != '\0' && s[i + 1] >= 48 && s[i + 1] <= 57)
			num *= 10;
		i++;
	}
	return (sign * num);
}
